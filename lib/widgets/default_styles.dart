import 'package:flutter/material.dart';

 
TextStyle defaultTextStyle(double _fontSize){
return  TextStyle(
      fontSize: _fontSize,
      color: Colors.white,
      fontFamily: 'Roboto',
    );
 }

 TextStyle defaultTextStyleColor(double _fontSize,Color color){
return  TextStyle(
      fontSize: _fontSize,
      color: color,
      fontFamily: 'Roboto',
    );
 }